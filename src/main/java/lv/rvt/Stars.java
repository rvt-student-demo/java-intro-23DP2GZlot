package lv.rvt;

import java.util.*;

public class Stars {

    public static void main (String[] args) {

        printStars(5);
        
        System.out.println("");
        
        printStars(3);
        
        System.out.println("");
    
        printStars(9);
        System.out.println("");
    
    
    }
    
        public static void printStars(int number) {
            for (int counter = 0; counter < number; counter++){
            
            System.out.print("*");
            
            }
        }
    }

    
    




    




          


        
        









